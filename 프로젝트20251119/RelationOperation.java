package 프로젝트20251119;

public class RelationOperation {
	public static void main(String[] args) {
		int x = 3, y = 5;
		
		System.out.println(x < y);
		System.out.println(x < y);
		System.out.println(x <= y);
		System.out.println(x >= y);
		System.out.println(x == y);
		System.out.println(x != y);
	}
}
